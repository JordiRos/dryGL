#include "dry.h"
#include "Material.h"

using namespace dry;


//------------------------------------------------------------------------------------------------
// Init
//
//------------------------------------------------------------------------------------------------
bool Material::Init(Shader *shader)
{
    bool res = false;
    m_Shader = shader;
    
    if (m_Shader)
    {
        res = true;
    }
    else
        dry::Log(LogWarning, "[Material] Can't initialize Material without Shader");
    return res;
}


//------------------------------------------------------------------------------------------------
// Free
//
//------------------------------------------------------------------------------------------------
void Material::Free()
{
    m_Shader = NULL;
}


